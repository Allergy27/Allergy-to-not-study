#include<bits/stdc++.h>

#define int int64_t /* R.I.P */
#define ln '\n'
#define judge(x) std::cout << ((x) ? "Yes\n" : "No\n");
#define lowbit(x) ((x)&-(x))
using ll = int64_t;
using PII = std::pair<int, int>;
const int inf = 0x3f3f3f3f;
const int ll_inf = 0x3f3f3f3f3f3f3f3f;

signed main(){
    std::cin.tie(nullptr) -> std::ios::sync_with_stdio(0);
    int _ = 1;

    auto solve = [&]() -> void{
        int n, m;
        std::cin >> n >> m;
        std::vector<int> qaq(n + 1);
        for(int i = 0; i < m; ++i){
            int l, r;
            std::cin >> l >> r;
            ++qaq[l - 1], --qaq[r];
        }
        int ans = qaq[0];
        for(int i = 1; i < n; ++i) {
            qaq[i] += qaq[i - 1];
            ans = std::min(ans, qaq[i]);
        }
        std::cout << ans << ln;
    };
    while(_--)solve();
    return 0;
}


